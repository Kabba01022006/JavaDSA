package _1_basics;

public class _4_variables {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        int y;
        y=20;
        System.out.println(y);
        System.out.println(x+" "+y);
        System.out.println(x*y);
        int z;
        z=1;
        System.out.println(z);
        z=2;
        System.out.println(z);
        z=3;
        System.out.println(z);
        z=z+5;
        System.out.println(z);
    }
}
