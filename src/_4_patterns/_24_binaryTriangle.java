package _4_patterns;

import java.util.Scanner;

public class _24_binaryTriangle {
    public static void main(String[] args) {
//        1
//        01
//        101
//        0101
//        10101
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the number of rows : ");
        int n = sc.nextInt();
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                if((i+j)%2==0){
                    System.out.print(1);
                }
                else if((i+j)%2!=0){
                    System.out.print(0);
                }
            }
            System.out.println();
        }
    }
}
